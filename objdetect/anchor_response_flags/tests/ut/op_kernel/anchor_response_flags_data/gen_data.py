import sys
import os
import numpy as np


def parse_str_to_shape_list(shape_str):
    shape_str = shape_str.strip('(').strip(')')
    shape_list = [int(x) for x in shape_str.split(",")]
    return np.array(shape_list)


def gen_data_and_golden(shape_str, d_type="float32", feat_h=60, feat_w=60,
                        stride_h=2, stride_w=2, num_base_anchors=9):
    d_type_dict = {
        "float32": np.float32,
        "float16": np.float16
    }
    np_type = d_type_dict[d_type]
    shape = parse_str_to_shape_list(shape_str)
    N = int(shape[0])

    # Generate random gt_bboxes with reasonable coordinate values
    # Coordinates in range [0, feat_size * stride) to ensure they map within feature map
    max_x = feat_w * stride_h
    max_y = feat_h * stride_w
    tmp_input = np.random.uniform(0, min(max_x, max_y), size=(N, 4)).astype(np_type)
    # Ensure x2 > x1 and y2 > y1 for valid bboxes
    for i in range(N):
        if tmp_input[i, 2] < tmp_input[i, 0]:
            tmp_input[i, 0], tmp_input[i, 2] = tmp_input[i, 2], tmp_input[i, 0]
        if tmp_input[i, 3] < tmp_input[i, 1]:
            tmp_input[i, 1], tmp_input[i, 3] = tmp_input[i, 3], tmp_input[i, 1]

    # Compute golden
    gt_bboxes_f32 = tmp_input.astype(np.float32)
    cx = (gt_bboxes_f32[:, 0] + gt_bboxes_f32[:, 2]) * 0.5
    cy = (gt_bboxes_f32[:, 1] + gt_bboxes_f32[:, 3]) * 0.5

    grid_x = np.floor(cx / stride_h).astype(np.int64)
    grid_y = np.floor(cy / stride_w).astype(np.int64)

    grid_x = np.clip(grid_x, 0, feat_w - 1)
    grid_y = np.clip(grid_y, 0, feat_h - 1)

    grid_idx = grid_y * feat_w + grid_x

    responsible_grid = np.zeros(feat_h * feat_w, dtype=np.uint8)
    responsible_grid[grid_idx] = 1

    tmp_golden = np.repeat(responsible_grid, num_base_anchors)

    tmp_input.astype(np_type).tofile(f"{d_type}_input_anchor_response_flags.bin")
    tmp_golden.astype(np.uint8).tofile(f"{d_type}_golden_anchor_response_flags.bin")


if __name__ == "__main__":
    if len(sys.argv) != 8:
        print("Usage: gen_data.py <shape> <dtype> <feat_h> <feat_w> <stride_h> <stride_w> <num_base_anchors>")
        print("Param num must be 7.")
        exit(1)
    # Clean bin files
    os.system("rm -rf *.bin")
    gen_data_and_golden(sys.argv[1], sys.argv[2],
                        int(sys.argv[3]), int(sys.argv[4]),
                        int(sys.argv[5]), int(sys.argv[6]),
                        int(sys.argv[7]))
