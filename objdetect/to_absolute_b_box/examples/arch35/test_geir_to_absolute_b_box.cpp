// normalized_boxes: fp32, shape (batch, num, 4), values in (0, 1)
static Tensor MakeBoxTensor(const vector<int64_t>& shape, TensorDesc& desc)
{
    desc = TensorDesc(ge::Shape(shape), FORMAT_ND, DT_FLOAT);
    desc.SetPlacement(ge::kPlacementHost);
    desc.SetFormat(FORMAT_ND);
    desc.SetRealDimCnt(shape.size());
    size_t n = 1;
    for (auto d : shape)
        n *= static_cast<size_t>(d);
    std::vector<float> buf(n);
    for (size_t i = 0; i < n; ++i) {
        buf[i] = 0.1f + 0.15f * static_cast<float>(i % 4); // 0.1, 0.25, 0.4, 0.55 cyclic
    }
    return Tensor(desc, reinterpret_cast<uint8_t*>(buf.data()), n * sizeof(float));
}

