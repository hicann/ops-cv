static int CreateOppInGraph(std::vector<ge::Tensor>& feeds, std::vector<Operator>& inputs,
                            std::vector<Operator>& outputs, Graph& graph)
{
    auto op1 = op::ToAbsoluteBBox("ToAbsoluteBBox");
    op1.SetAttr("reversed_box", false);

    // input 1: normalized_boxes (fp32, [2,5,4], data index 0)
    vector<int64_t> boxShape = {2, 5, 4};
    TensorDesc boxDesc;
    Tensor boxTensor = MakeBoxTensor(boxShape, boxDesc);
    auto dataBoxes = op::Data("normalized_boxes").set_attr_index(0);
    dataBoxes.update_input_desc_x(boxDesc);
    dataBoxes.update_output_desc_y(boxDesc);
    graph.AddOp(dataBoxes);
    op1.set_input_normalized_boxes(dataBoxes);
    op1.update_input_desc_normalized_boxes(boxDesc);
    feeds.push_back(boxTensor);
    inputs.push_back(dataBoxes);

    // input 2: shape_hw (int32, [4]=[2,100,200,3], data index 1)
    TensorDesc shwDesc;
    Tensor shwTensor = MakeShwTensor(shwDesc);
    auto dataShw = op::Data("shape_hw").set_attr_index(1);
    dataShw.update_input_desc_x(shwDesc);
    dataShw.update_output_desc_y(shwDesc);
    graph.AddOp(dataShw);
    op1.set_input_shape_hw(dataShw);
    op1.update_input_desc_shape_hw(shwDesc);
    feeds.push_back(shwTensor);
    inputs.push_back(dataShw);

    // output: y (dtype/shape == normalized_boxes)
    TensorDesc yDesc = TensorDesc(ge::Shape(boxShape), FORMAT_ND, DT_FLOAT);
    op1.update_output_desc_y(yDesc);

    outputs.push_back(op1);
    return SUCCESS;
}

