static ge::graphStatus ComputeTilingParams(int64_t dim0, int64_t availCoreNum, uint64_t ubSize, ge::DataType dtype,
                                           int64_t* coreNum, int64_t* blockFormer, int64_t* blockNum, int64_t* ubFormer,
                                           int64_t* ubLoopOfFormerBlock, int64_t* ubTailOfFormerBlock,
                                           int64_t* ubLoopOfTailBlock, int64_t* ubTailOfTailBlock)
{
    // TilingData 计算（物理正确量纲）
    int64_t elemBytes = (dtype == ge::DT_FLOAT16) ? FP16_BYTES : FP32_BYTES;
    int64_t minDtypeBits = elemBytes * BITS_PER_BYTE;

    // dim0*minDtypeBits 理论 int64 溢出需 dim0 > 2.8e17（物理内存不可达），且上游已有 dim0<=0 兜底，
    //   故不加额外上界护栏（检视 L2）。
    *coreNum = CeilDiv(dim0 * minDtypeBits, MIN_TILING_BITS);
    *coreNum = std::min(*coreNum, availCoreNum);
    if (*coreNum < 1) {
        *coreNum = 1;
    }
    *blockFormer = CeilAlign(CeilDiv(dim0, *coreNum), ELEM_ALIGN_FACTOR); // 512 元素对齐
    *blockNum = CeilDiv(dim0, *blockFormer);

    int64_t bufferDivisor = BUFFER_NUM * elemBytes;
    int64_t maxElemNum = (static_cast<int64_t>(ubSize) - EXTRA_SIZE) / bufferDivisor;
    int64_t alignFactor = ALIGN_256 / elemBytes; // fp16=128, fp32=64
    *ubFormer = (maxElemNum / alignFactor) * alignFactor;
    if (*ubFormer < alignFactor) {
        *ubFormer = alignFactor;
    }

    *ubLoopOfFormerBlock = CeilDiv(*blockFormer, *ubFormer);
    *ubTailOfFormerBlock = *blockFormer - (*ubLoopOfFormerBlock - 1) * (*ubFormer);
    int64_t blockTail = dim0 - (*blockNum - 1) * (*blockFormer);
    *ubLoopOfTailBlock = CeilDiv(blockTail, *ubFormer);
    *ubTailOfTailBlock = blockTail - (*ubLoopOfTailBlock - 1) * (*ubFormer);

    return ge::GRAPH_SUCCESS;
}

